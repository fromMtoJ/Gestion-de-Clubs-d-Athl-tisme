document.getElementById('titre_2_1').addEventListener('click', function() {
    var action1 = document.getElementById('action_1');
    if (action1.style.display === 'none' || action1.style.display === '') { 
        action1.style.display = 'block';
      } else {
        action1.style.display = 'none';
    }
})

